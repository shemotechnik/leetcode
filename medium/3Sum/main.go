func threeSum(nums []int) [][]int {
	start := time.Now()
	type triplet struct {
		n1, n2, n3 int
	}

	triplets := map[triplet]struct{}{}

	if len(nums) < 3 {
		return [][]int{}
	}
	sort.Ints(nums)
	for i := 0; i < len(nums)-2; i++ {
		l, r := i+1, len(nums)-1
		for l < r {
			sum := nums[i] + nums[l] + nums[r]
			if sum == 0 {
				triplets[triplet{nums[i], nums[l], nums[r]}] = struct{}{}
				l++
				r--
			} else if sum < 0 {
				l++
			} else {
				r--
			}
		}
	}
	result := make([][]int, 0, len(triplets))
	for t := range triplets {
		result = append(result, []int{t.n1, t.n2, t.n3})
	}
	elapsed := time.Since(start)
	fmt.Println(elapsed)
	return result
}
